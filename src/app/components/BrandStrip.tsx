export function BrandStrip() {
  const scrollRef = useRef<HTMLDivElement>(null);
  const [isDragging, setIsDragging] = useState(false);
  const [startX, setStartX] = useState(0);
  const [scrollLeft, setScrollLeft] = useState(0);

  const startDragging = (e: React.MouseEvent) => {
    setIsDragging(true);
    if (scrollRef.current) {
      setStartX(e.pageX - scrollRef.current.offsetLeft);
      setScrollLeft(scrollRef.current.scrollLeft);
    }
  };

  const stopDragging = () => {
    setIsDragging(false);
  };

  const onDrag = (e: React.MouseEvent) => {
    if (!isDragging || !scrollRef.current) return;
    e.preventDefault();
    const x = e.pageX - scrollRef.current.offsetLeft;
    const walk = (x - startX) * 1.5;
    scrollRef.current.scrollLeft = scrollLeft - walk;
  };

  return (
    <div className="bg-white border-y border-[#d0d5dd]/50 overflow-hidden">
      <div 
        ref={scrollRef}
        className={`max-w-[1197px] mx-auto border-x border-[#e5e7eb] py-6 flex overflow-x-auto whitespace-nowrap scrollbar-hide select-none group/strip ${isDragging ? 'cursor-grabbing' : 'cursor-grab'}`}
        onMouseDown={startDragging}
        onMouseLeave={stopDragging}
        onMouseUp={stopDragging}
        onMouseMove={onDrag}
        style={{ scrollbarWidth: 'none', msOverflowStyle: 'none' }}
      >
        <style dangerouslySetInnerHTML={{ __html: `
          .scrollbar-hide::-webkit-scrollbar { display: none; }
        ` }} />
        <div className="flex w-max items-center gap-[100px] px-8 sm:px-12">
          {brands.map(({ name, component: Logo }, idx) => (
            <div 
              key={`${name}-${idx}`} 
              className="flex-shrink-0 transition-all duration-300 group-hover/strip:grayscale hover:!grayscale-0 opacity-100 group-hover/strip:opacity-50 hover:!opacity-100"
            >
              <Logo />
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}
